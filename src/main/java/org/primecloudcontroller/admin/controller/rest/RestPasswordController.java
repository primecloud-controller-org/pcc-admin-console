package org.primecloudcontroller.admin.controller.rest;

import org.springframework.security.crypto.bcrypt.BCryptPasswordEncoder;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;

@RestController
public class RestPasswordController extends AbstractRestController {

    private PasswordEncoder encoder = new BCryptPasswordEncoder();

    @RequestMapping(path = "/rest/password/encode", method = RequestMethod.POST)
    public RestResponse encode(@RequestParam(name = "password", required = false) String password) {
        String encodedPassword = encoder.encode(password);

        return new RestResponse(encodedPassword);
    }

}
